#include "libft.h"

int		ft_isalnum(int c)
{
	unsigned char	sym;

	sym = c;
	if ((c >= '0' && c <= '9') || (c >= 'A' && c <= 'Z') ||
			(c >= 'a' && c <= 'z'))
		return (1);
	return (0);
}
